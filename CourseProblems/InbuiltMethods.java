package CourseProblems;

public class InbuiltMethods {
  public static void main(String[] args) {
    // Random number
    System.out.println(Math.random());

    // Minimum number
    // Accept two arguments
    System.out.println(Math.min(23, 17));

    // Maximum number
    // Accept two arguments
    System.out.println(Math.max(23, 17));
  }
}
