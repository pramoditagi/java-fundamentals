package CoursePrograms;

public class InbuiltMethods {
  public static void main(String[] args) {
    /*
     * Math.random: This generates the number between 0 and 1
     * it will not take any parameters as an argument
     * It should have () brackets as methods have ()
     */
    System.out.println(Math.random());

    /*
     * Math.max(firstNumber, secondNumber)
     * This is used to get the maximum number from the given 2 numbers
     * It will accept 2 arguments and returns maximum number
     */
    System.out.println(Math.max(3, 5));

    /*
     * Math.min(firstNumber, secondNumber)
     * This is used to get the minimum number from the given 2 numbers
     * It will accept 2 arguments and returns minimum number
     */
    System.out.println(Math.min(3, 5));
  }
}
