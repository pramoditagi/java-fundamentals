package PracticePrograms;

public class ExpressionForMinutesSeconds {
  public static void main(String[] args) {
    int minutes = 24 * 60;
    System.out.println("Number of minutes in a day:" + minutes);

    int seconds = 60 * 60 * 24;
    System.out.println("Number of seconds in a day:" + seconds);
  }
}
