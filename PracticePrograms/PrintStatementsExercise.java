package PracticePrograms;

public class PrintStatementsExercise {
  public static void main(String[] args) {
    // Print Hello World
    System.out.println("Hello World");

    // Print 5 * 3 as is
    System.out.println("5 * 3");

    // Print calculated value of 5 * 3
    // Note: Do not enclose them in "" as we want to evaluate an expression
    System.out.println(5 * 3);

    // Print number of seconds in a day
    System.out.println("Number of seconds in a day:" + 60 * 60 * 24);
  }
}
